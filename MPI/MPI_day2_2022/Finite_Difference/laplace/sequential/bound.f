      subroutine bound (nx,ny,u) 
c-----------------------------------------------------------------------
c  Dirichlet boundary conditions
c-----------------------------------------------------------------------
      integer i, j
      integer nx, ny
      double precision u(0:nx+1,0:ny+1)
c-----------------------------------------------------------------------
c  left and right boundary condition
c-----------------------------------------------------------------------
      do 10 j = 1,ny
         u(   0,j) = 1.0d0
         u(nx+1,j) = 0.0d0
   10 continue
c-----------------------------------------------------------------------
c  lower and upper boundary condition
c-----------------------------------------------------------------------
      do 20 i = 1,nx
         u(i,0)    = 1.0d0
         u(i,ny+1) = 0.0d0
   20 continue
c-----------------------------------------------------------------------
c  end
c-----------------------------------------------------------------------
      return
      end
